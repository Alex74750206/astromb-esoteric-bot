def _get_coords(city: str) -> tuple[float, float]:
    key = city.lower().strip()
    for k, v in CITY_COORDS.items():
        if k in key or key in k:
            return v
    return (55.75, 37.62)  # Москва по умолчанию


def _ecl_lon_to_sign(lon_deg: float) -> str:
    idx = int(lon_deg // 30) % 12
    return SIGNS_RU[idx]


def _planet_sign(planet, obs) -> str:
    planet.compute(obs)
    ecl = ephem.Ecliptic(planet, epoch=obs.date)
    lon = math.degrees(ecl.lon) % 360
    return _ecl_lon_to_sign(lon)


def _calc_ascendant(obs) -> str:
    """Асцендент через RAMC (Right Ascension of Midheaven)."""
    try:
        ramc = ephem.degrees(obs.sidereal_time())
        lat_rad = obs.lat
        # Уравнение для Асцендента
        ramc_deg = math.degrees(float(ramc))
        lat_deg = math.degrees(float(lat_rad))
        lat_r = math.radians(lat_deg)
        eps = math.radians(23.4397)  # наклон эклиптики
        ramc_r = math.radians(ramc_deg)
        y = -math.cos(ramc_r)
        x = math.sin(eps) * math.tan(lat_r) + math.cos(eps) * math.sin(ramc_r)
        asc_lon = math.degrees(math.atan2(y, x)) % 360
        return _ecl_lon_to_sign(asc_lon)
    except Exception:
        return "Овен"


def _calc_planets(birth_date: str, birth_time: str, birth_city: str) -> dict:
    p = birth_date.replace("-", ".").split(".")
    d, mo, y = int(p[0]), int(p[1]), int(p[2])
    try:
        tp = birth_time.replace(".", ":").split(":")
        h, mi = int(tp[0]), int(tp[1]) if len(tp) > 1 else 0
    except Exception:
        h, mi = 12, 0

    lat, lon = _get_coords(birth_city)

    obs = ephem.Observer()
    obs.lat = str(lat)
    obs.lon = str(lon)
    obs.date = f"{y}/{mo}/{d} {h}:{mi}:00"
    obs.pressure = 0

    planets = {}
    for name, body in [
        ("Солнце", ephem.Sun()),
        ("Луна", ephem.Moon()),
        ("Меркурий", ephem.Mercury()),
        ("Венера", ephem.Venus()),
        ("Марс", ephem.Mars()),
        ("Юпитер", ephem.Jupiter()),
        ("Сатурн", ephem.Saturn()),
    ]:
        planets[name] = _planet_sign(body, obs)

    planets["Асцендент"] = _calc_ascendant(obs)
    planets["_hour"] = h
    planets["_minute"] = mi
    return planets



def expression_number(name: str) -> int:
    total = sum(LETTER_VALUES.get(c.upper(), 0) for c in name if c.isalpha())
    return _reduce(total) or 9


def soul_urge(name: str) -> int:
    total = sum(LETTER_VALUES.get(c.upper(), 0) for c in name if c in VOWELS)
    return _reduce(total) or 9


def _reduce_matrix(n: int) -> int:
    while n > 22:
        n = sum(int(d) for d in str(n))
    return n or 22


def calculate_matrix(birth_date: str) -> dict:
    parts = birth_date.replace("-", ".").split(".")
    day, month, year = int(parts[0]), int(parts[1]), int(parts[2])

    d = _reduce_matrix(day)
    m = _reduce_matrix(month)
    y = _reduce_matrix(sum(int(c) for c in str(year)))
    fate = _reduce_matrix(d + m + y)

    sky = _reduce_matrix(d + m)
    earth = _reduce_matrix(m + y)
    past = _reduce_matrix(d + y)
    future = _reduce_matrix(d + m + y + fate)
    center = _reduce_matrix(sky + earth)

    return {
        "soul": d,
        "body": m,
        "spirit": y,
        "fate": fate,
        "sky_self": sky,
        "earth_self": earth,
        "past_karma": past,
        "future_karma": future,
        "center": center,
    }



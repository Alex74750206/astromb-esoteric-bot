def get_zodiac(birth_date: str) -> tuple[str, str, str]:
    parts = birth_date.replace("-", ".").split(".")
    day, month = int(parts[0]), int(parts[1])
    for name, start, end, forecast, shadow in ZODIAC:
        sm, sd = start
        em, ed = end
        if (month == sm and day >= sd) or (month == em and day <= ed):
            return name, forecast, shadow
    return "Козерог", ZODIAC[0][3], ZODIAC[0][4]



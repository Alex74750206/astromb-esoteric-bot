# ── Расчёты ───────────────────────────────────────────────────────────────────

def year_pillar(year: int) -> tuple[int, int]:
    return (year - 4) % 10, (year - 4) % 12


def month_pillar(year: int, month: int) -> tuple[int, int]:
    year_stem = (year - 4) % 10
    branch_idx = (month + 1) % 12
    stem_idx = (year_stem * 2 + month + 1) % 10
    return stem_idx, branch_idx


def day_pillar(year: int, month: int, day: int) -> tuple[int, int]:
    # Reference: Jan 1, 2000 = 戊午 (stem=4, branch=6)
    ref = Date(2000, 1, 1)
    delta = (Date(year, month, day) - ref).days
    return (delta + 4) % 10, (delta + 6) % 12


def count_elements(pillars: list[tuple[int, int]]) -> dict[str, int]:
    counts: dict[str, int] = {e: 0 for e in ELEMENT_DESC}
    for s, b in pillars:
        counts[STEM_ELEM[s]] += 1
        counts[BRANCH_ELEM[b]] += 1
    return counts


def dominant_element(counts: dict[str, int]) -> str:
    return max(counts, key=lambda e: counts[e])


